
public class GenelUye {

	private String eMail, ad, soyad, mesaj;
	private int sifre;
	

	public void setEMail(String eMail) {
		this.eMail = eMail;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public void setSoyad(String soyad) {
		this.soyad = soyad;
	}
	public void setMesaj(String mesaj) {
		this.mesaj = mesaj;
	}
	public void setSifre(int sifre) {
		this.sifre = sifre;
	}
	
	public String getEMail() {
		return eMail;
	}
	public String getAd() {
		return ad;
	}
	public String getSoyad() {
		return soyad;
	}
	public String getMesaj() {
		return mesaj;
	}
	public int getSifre() {
		return sifre;
	}
	
}
